//Exercise three, a program that prints the absolute value of an integer

public class sheet_one_three
{
   public static void main (String[] args)
   {
      int num = -2;
      int abs = (num)*-1;
   /* the absolute value of a positive number is itself, however the absolute
   value of a negative number is the number multiplied by -1 */
      System.out.println( "the absolute value of " + num + " is " + abs);
   }
}